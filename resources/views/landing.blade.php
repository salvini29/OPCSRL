    $(document).ready(function() {
      $('#postComment').validate({
        rules: {
          nombre : {
            required: true,
            minlength: 4
          },
          email: {
            required: true,
            email: true
          },
          asunto: {
            required: true,
            minlength: 8
          },
          mensaje: {
            required: true,
            minlength: 8
          }
        }
      });
    });

    $('#postComment').submit(function(e){
      
      if ($('#postComment').valid()) { 
        e.preventDefault();
        var nombre = $("input[name=nombre]").val();
        var email = $("input[name=email]").val();
        var asunto = $("input[name=asunto]").val();
        var mensaje = $("#mensaje").val();
        var _token = $("input[name=_token]").val();
        $.ajax({
          url:"{{ route('postComment') }}",
          type:"POST",
          data:{
            nombre:nombre,
            email:email,
            asunto:asunto,
            mensaje:mensaje,
            _token:_token
          },
          success:function(response){
            console.log(response)
            $('#postComment').trigger("reset");
            Swal.fire({
              title: 'Éxito!',
              text: 'El mensaje se ha mandado!',
              icon: 'success',
              showConfirmButton: true,
              timer: 3000
            })
          },
          error: function(xhr, textStatus, error){
            $('#postComment').trigger("reset");
            Swal.fire({
              title: 'Error!',
              text: 'El mensaje no se ha mandado!',
              icon: 'error',
              showConfirmButton: true,
              timer: 3000
            })
          }
        })
      }
    })

